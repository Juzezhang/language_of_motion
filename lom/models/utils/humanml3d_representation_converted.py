def linear_interpolate_fps(data, original_fps, target_fps):
    """
    Linearly interpolate 3D skeleton motion data from one FPS to another.

    Parameters:
    data (numpy array): Original motion data of shape (num_frames, num_joints, 3)
    original_fps (int): The original frames per second, e.g., 20
    target_fps (int): The target frames per second, e.g., 30

    Returns:
    numpy array: Interpolated motion data at the new frame rate with shape (target_num_frames, num_joints, 3).
    """
    original_num_frames = data.shape[0]
    target_num_frames = int(original_num_frames * (target_fps / original_fps))

    # Generate indices for original and target frames
    original_indices = np.arange(original_num_frames)
    target_indices = np.linspace(0, original_num_frames - 1, target_num_frames)

    # Perform linear interpolation across frames for each joint's coordinates
    interpolated_data = np.empty((target_num_frames, data.shape[1], data.shape[2]))
    for joint in range(data.shape[1]):
        for coord in range(data.shape[2]):
            # Interpolate for each joint and coordinate independently
            interpolated_data[:, joint, coord] = np.interp(target_indices, original_indices, data[:, joint, coord])

    return interpolated_data



